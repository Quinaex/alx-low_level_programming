#include <stdio.h>
/**
 * main - A program that prints the alphabet in lowercase and in uppercase
 * Return: 0 (success)
 */

int main(void)
{
	char x;
	char y;

	x = 'a';
	y = 'A';
	while
		(x <= 'z') {
			putchar(x);
			x++;
		}
		(y <= 'z'); {
			putchar(y);
			y++;
		}
	putchar('\n');
	return (0);
}
